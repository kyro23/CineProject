package model;

public class FilmModel {
	private int id;
	private String title;
	private String actors;
	private String sinopse;
	private String genere;
	private int duraction;
	private String directors;
	private String classindicative;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getActors() {
		return actors;
	}
	public void setActors(String actors) {
		this.actors = actors;
	}
	public String getSinopse() {
		return sinopse;
	}
	public void setSinopse(String sinopse) {
		this.sinopse = sinopse;
	}
	public String getGenere() {
		return genere;
	}
	public void setGenere(String genere) {
		this.genere = genere;
	}
	public int getDuraction() {
		return duraction;
	}
	public void setDuraction(int duraction) {
		this.duraction = duraction;
	}
	public String getDirectors() {
		return directors;
	}
	public void setDirectors(String directors) {
		this.directors = directors;
	}
	public String getClassindicative() {
		return classindicative;
	}
	public void setClassindicative(String classindicative) {
		this.classindicative = classindicative;
	}
	

}
